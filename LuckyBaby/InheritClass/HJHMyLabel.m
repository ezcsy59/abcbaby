#import "HJHMyLabel.h"

@implementation HJHMyLabel
-(id)init{
    if (self == [super init]) {
        [self setBackgroundColor:[UIColor clearColor]];
    }
    return self;
}

-(id)initWithFrame:(CGRect)frame{
    if (self == [super initWithFrame:frame]) {
        [self setBackgroundColor:[UIColor clearColor]];
    }
    return self;
}

-(id)initWithCoder:(NSCoder *)aDecoder{
    if (self == [super initWithCoder:aDecoder]) {
        [self setBackgroundColor:[UIColor clearColor]];
    }
    return self;
}
@end
